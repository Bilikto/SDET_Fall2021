package interview_questions_Baha.solutions;

public class NumericFormatPrinting {

    public static void main(String[] args) {
        // print string
        System.out.format("Hello my name is %s%n", "Billy");
        System.out.println("Hello my name is Billy");

        // print format float numbers
        System.out.printf("The value of PI is %.2f%n", 3.14); //format specifier

        //print integers
        System.out.printf("Year is %d", 2021);




    }






}
