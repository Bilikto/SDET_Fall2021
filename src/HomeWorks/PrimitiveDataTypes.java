package HomeWorks;

public class PrimitiveDataTypes {
    public static void main(String[] args) {
        int intVar = 10;
        float floatVar = 12.5F;
        String strVar = "Java programming";
        System.out.println("Integer value is: " + intVar);
        System.out.println("Float value is " + floatVar);
        System.out.println("String value is " + "'" + strVar + "'");

        System.out.println("====================================");
        int roomNum = 113;
        double eNum = 2.71828;
        String myLang = "Computer Science";
        System.out.println("This is room #" + roomNum);
        System.out.println("e is close to " + eNum);
        System.out.println("I am learning a bit about " + "'" + myLang + "'");

    }
}
