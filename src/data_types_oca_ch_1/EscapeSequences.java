package data_types_oca_ch_1;

public class EscapeSequences {

    public static void main(String[] args) {
        //\" -> double quotes
        String welcome = "Welcome to Google. This is the \"headquarters\" of our company!";
        System.out.println(welcome);

        //\n -> new line
        String welcome2 = "Welcome to Google. \nThis is the \"headquarters\" of our company!";
        System.out.println(welcome2);

        //\t -> tab
        System.out.println("\tHello all, please meet Sundar Pichai");




    }

}
